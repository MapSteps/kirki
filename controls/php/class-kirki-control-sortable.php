/**
 * Sortable control (uses checkboxes).
 */
class Kirki_Control_Sortable extends Kirki_Control_Base {

	/**
	 * The control type.
	 *
	 * @access public
	 * @var string
	 */
	public $type = 'kirki-sortable';
	
	/**
	 * Mode.
	 *
	 * @var string
	 */
	public $mode = 'checkbox';
	
	/**
	 * Toggles visibility icons on and off
	 * @var string
	 */
	public $visibility_toggle = true;
	
	public function to_json() {
		parent::to_json();
		
		$this->json['mode'] = $this->mode;
		$this->json['visibility_toggle'] = $this->visibility_toggle;
	}

	/**
	 * An Underscore (JS) template for this control's content (but not its container).
	 *
	 * Class variables for this control class are available in the `data` JS object;
	 * export custom variables by overriding {@see WP_Customize_Control::to_json()}.
	 *
	 * @see WP_Customize_Control::print_template()
	 *
	 * @access protected
	 */
	protected function content_template() {
		?>
		<label class='kirki-sortable'>
			<span class="customize-control-title">
				{{{ data.label }}}
			</span>
			<# if ( data.description ) { #>
				<span class="description customize-control-description">{{{ data.description }}}</span>
			<# } #>
			<ul class="sortable"></ul>
		</label>
		<?php
	}
}